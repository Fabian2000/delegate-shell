/// Migrate a bash/sh script to dgsh syntax.
/// Returns the converted source code.
#[must_use]
pub fn migrate_sh_to_dgsh(source: &str) -> String {
    let mut ctx = MigrateContext::new();
    let lines: Vec<&str> = source.lines().collect();

    // First pass: collect user-defined function names
    for line in &lines {
        let trimmed = line.trim();
        if let Some(name) = parse_function_def(trimmed) {
            ctx.user_functions.push(name.to_ascii_lowercase());
        }
    }

    let mut i = 0;
    while i < lines.len() {
        let line = lines[i];
        let trimmed = line.trim();
        i += 1;

        // Skip shebang
        if trimmed.starts_with("#!") && ctx.output.is_empty() {
            ctx.push_line("# Auto-generated by dgsh -migrate");
            ctx.push_line("# This file may contain errors and should be reviewed manually.");
            ctx.push_line("# Lines marked with '# TODO' require manual adjustment.");
            ctx.push_line("");
            continue;
        }

        // Heredoc collection mode
        if let Some(ref marker) = ctx.heredoc_marker.clone() {
            if trimmed == marker {
                // End of heredoc — emit as """ string
                let content = ctx.heredoc_lines.join("\n");
                let cmd = ctx.heredoc_cmd.clone();
                ctx.heredoc_marker = None;
                ctx.heredoc_lines.clear();
                ctx.heredoc_cmd.clear();
                if cmd.is_empty() {
                    ctx.push_line("\"\"\"");
                    ctx.push_raw(&content);
                    ctx.push_line("\"\"\"");
                } else {
                    ctx.push_line(&format!("# Heredoc content for: {cmd}"));
                    ctx.push_line("_heredoc_content = \"\"\"");
                    ctx.push_raw(&content);
                    ctx.push_line("\"\"\"");
                    let (cmd_stripped, _) = strip_redirects(&cmd);
                    let converted = convert_command(cmd_stripped.trim());
                    ctx.push_line(&format!("# TODO: pass _heredoc_content to: {converted}"));
                }
            } else {
                ctx.heredoc_lines.push(line.to_string());
            }
            continue;
        }

        // Empty lines
        if trimmed.is_empty() {
            ctx.push_raw("");
            continue;
        }

        // Comments
        if trimmed.starts_with('#') {
            ctx.push_indented(trimmed);
            continue;
        }

        // Process the line
        ctx.process_line(trimmed);
    }

    // Close any remaining open blocks
    while ctx.indent_level > 0 {
        ctx.indent_level -= 1;
    }

    ctx.output.join("\n") + "\n"
}

